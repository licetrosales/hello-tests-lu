public class Sum {
    // public = für alle einsesehbar
    // static = Methode einer Klasse
    // int = Datentyp der zurückgegeben wird
    //sum = Nmae der Methode
    // (int a, int b) = Parameter der Methode = Daten die wir der Methode geben

    public static int sum(int a, int b){
        int sumOfAPlusB = a+b;
        return sumOfAPlusB;
    }
}
